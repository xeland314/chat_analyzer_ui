// Regex para tokenizaci√≥n simple (dividir por espacios y puntuaci√≥n)
lazy_static! {
    static ref TOKEN_PATTERN: Regex = Regex::new(r"(\w+|[^\s\w])").unwrap();
}

// Funci√≥n principal de an√°lisis de sentimiento
pub fn get_sentiment_score(text: &str) -> f32 {
    let lower_text = text.to_lowercase();

    let mut score = 0.0;
    let mut count = 0;
    let mut negated = false;
    let mut intensifier_factor = 1.0;

    let tokens: Vec<String> = TOKEN_PATTERN
        .find_iter(&lower_text)
        .map(|m| m.as_str().to_string())
        .collect();

    for token in tokens.iter() {
        // 1. Detecci√≥n de Modificadores (VADER-Lite)
        let is_negation = NEGATIONS.contains(token.as_str());
        let is_intensifier = LEXICON
            .get(token.as_str())
            .map_or(false, |&s| s.abs() < 1.0);

        if is_negation {
            negated = true;
            continue;
        }

        if is_intensifier {
            // Usamos el score del l√©xico como factor intensificador (Ej: "muy" = 0.5 -> factor 1.5)
            intensifier_factor += LEXICON.get(token.as_str()).unwrap().abs();
            continue;
        }

        // 2. Detecci√≥n de Sentimiento (L√©xico)
        let mut word_score = 0.0;

        if let Some(&s) = LEXICON.get(token.as_str()) {
            word_score = s;
        } else if let Some(&s) = EMOJI_SENTIMENT.get(token.as_str()) {
            word_score = s;
        }

        if word_score != 0.0 {
            // Aplicar Intensificaci√≥n
            word_score *= intensifier_factor;

            // Aplicar Negaci√≥n (Invierte signo y reduce impacto)
            if negated {
                word_score *= -1.0;
                word_score *= 0.75; // Reducir un poco el impacto de la inversi√≥n
            }

            score += word_score;
            count += 1;

            // Reset de modificadores despu√©s de aplicarlos
            negated = false;
            intensifier_factor = 1.0;
        }
    }

    // 3. Reglas de Puntuaci√≥n/May√∫sculas (VADER-Lite)
    let mut multiplier = 1.0;

    // Puntuaci√≥n (Exclamaciones)
    if text.contains("!!!") {
        multiplier *= 1.3;
    } else if text.contains("!!") {
        multiplier *= 1.1;
    }

    // May√∫sculas (GRITAR)
    if text.to_uppercase() == text && text.len() > 5 {
        multiplier *= 1.2;
    }

    score *= multiplier;

    // 4. Normalizar el score: Satura entre -1.0 y 1.0.
    if count == 0 {
        return 0.0;
    }

    // Simplemente normalizar por recuento y saturar
    let normalized = score / (count as f32).sqrt();
    normalized.min(1.0).max(-1.0)
}
